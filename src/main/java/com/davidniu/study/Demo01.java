package com.davidniu.study;

public class Demo01 {
    public static void main(String[] args) {
        int a = 10;
        int b = a++;
        int c = ++a;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        double d = Math.pow(2, 3);
        System.out.println("2^3 = " + d);
    }
}
